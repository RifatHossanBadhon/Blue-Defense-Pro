-- ============================================================
--  STORED PROCEDURE: Calculate_Z_Score_And_Alert
--  Called after every sighting insert.
--  Calculates Z-Score over last 7 days for a given indicator.
--  If Z-Score > 3.0, inserts a Critical anomaly record.
-- ============================================================

DROP PROCEDURE IF EXISTS Calculate_Z_Score_And_Alert;

DELIMITER $$

CREATE PROCEDURE Calculate_Z_Score_And_Alert(IN p_indicator_id INT)
BEGIN
    DECLARE v_avg       DECIMAL(10,2) DEFAULT 0;
    DECLARE v_std       DECIMAL(10,2) DEFAULT 0;
    DECLARE v_recent    INT           DEFAULT 0;
    DECLARE v_z_score   DECIMAL(10,2)  DEFAULT 0;
    DECLARE v_severity  VARCHAR(20);
    DECLARE v_reason    VARCHAR(255);

    -- Step 1: Get avg and stddev of sighting counts over last 30 days
    SELECT
        COALESCE(AVG(count), 0),
        COALESCE(STDDEV(count), 0)
    INTO v_avg, v_std
    FROM SIGHTINGS
    WHERE indicator_id = p_indicator_id
      AND timestamp >= NOW() - INTERVAL 7 DAY;

    -- Step 2: Get the most recent sighting count for this indicator
    SELECT count INTO v_recent
    FROM SIGHTINGS
    WHERE indicator_id = p_indicator_id
    ORDER BY timestamp DESC
    LIMIT 1;

    -- Step 3: Calculate Z-Score (guard against division by zero)
    IF v_std > 0 THEN
        SET v_z_score = (v_recent - v_avg) / v_std;
    ELSE
        SET v_z_score = 0;
    END IF;

    -- Step 4: Insert anomaly if Z-Score exceeds threshold
    IF v_z_score > 3.0 THEN
        SET v_severity = 'Critical';
        SET v_reason   = CONCAT(
            'Count=', v_recent,
            ' vs avg=', ROUND(v_avg,1),
            ' stddev=', ROUND(v_std,1),
            ' over last 7 days'
        );

        INSERT INTO ANOMALIES (indicator_id, severity, detection_reason, z_score)
        VALUES (p_indicator_id, v_severity, v_reason, v_z_score);
    END IF;

END$$

DELIMITER ;

-- ============================================================
--  TRIGGER: after_sighting_insert
--  Automatically invokes Z-Score calculation upon new sightings.
-- ============================================================

DROP TRIGGER IF EXISTS after_sighting_insert;

DELIMITER $$
CREATE TRIGGER after_sighting_insert
AFTER INSERT ON SIGHTINGS
FOR EACH ROW
BEGIN
    -- Evaluate anomaly detection automatically
    CALL Calculate_Z_Score_And_Alert(NEW.indicator_id);
END$$
DELIMITER ;

