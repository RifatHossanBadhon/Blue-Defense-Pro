CREATE DATABASE IF NOT EXISTS blue_db;
USE blue_db;

-- ── TABLE: SOURCES ──────────────────────────────────────────
-- Stores the threat intelligence feed sources (e.g. URLhaus)
CREATE TABLE IF NOT EXISTS SOURCES (
    source_id        INT AUTO_INCREMENT PRIMARY KEY,
    name             VARCHAR(100) NOT NULL UNIQUE,
    reliability_rating INT,
    description      VARCHAR(255)
);

-- ── TABLE: THREAT_ACTORS ────────────────────────────────────
-- Known adversary groups linked to indicators
CREATE TABLE IF NOT EXISTS THREAT_ACTORS (
    actor_id         INT AUTO_INCREMENT PRIMARY KEY,
    name             VARCHAR(100) NOT NULL UNIQUE,
    origin_country   VARCHAR(50),
    target_sector    VARCHAR(100),
    description      TEXT
);

-- ── TABLE: INDICATORS ───────────────────────────────────────
-- Core IOC records (IPs, URLs, Hashes, Domains)
CREATE TABLE IF NOT EXISTS INDICATORS (
    indicator_id     INT AUTO_INCREMENT PRIMARY KEY,
    type             ENUM('IP', 'URL', 'Hash', 'Domain') NOT NULL,
    value            VARCHAR(255) NOT NULL UNIQUE,
    actor_id         INT NULL,
    confidence_score INT DEFAULT 50,
    first_seen       DATETIME DEFAULT CURRENT_TIMESTAMP,
    last_seen        DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (actor_id) REFERENCES THREAT_ACTORS(actor_id) ON DELETE SET NULL
);

-- ── TABLE: SIGHTINGS ────────────────────────────────────────
-- Every time an indicator is seen from a source, log it here
CREATE TABLE IF NOT EXISTS SIGHTINGS (
    sighting_id      INT AUTO_INCREMENT PRIMARY KEY,
    indicator_id     INT NOT NULL,
    source_id        INT NOT NULL,
    timestamp        DATETIME DEFAULT CURRENT_TIMESTAMP,
    count            INT DEFAULT 1,
    FOREIGN KEY (indicator_id) REFERENCES INDICATORS(indicator_id) ON DELETE CASCADE,
    FOREIGN KEY (source_id)   REFERENCES SOURCES(source_id)    ON DELETE CASCADE
);

-- ── TABLE: ANOMALIES ────────────────────────────────────────
-- Stores alerts generated by the Z-Score stored procedure
CREATE TABLE IF NOT EXISTS ANOMALIES (
    anomaly_id       INT AUTO_INCREMENT PRIMARY KEY,
    indicator_id     INT NOT NULL,
    severity         ENUM('Low', 'Medium', 'High', 'Critical') NOT NULL,
    detection_reason VARCHAR(255) NOT NULL,
    z_score          DECIMAL(10,2) NOT NULL,
    detected_at      DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (indicator_id) REFERENCES INDICATORS(indicator_id) ON DELETE CASCADE
);

